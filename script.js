var map, heatmap;

      function initMap() {
        map = new google.maps.Map(document.getElementById('map'), {
          zoom: 13,
          center: {lat: 42.361, lng: -71.057},
         //mapTypeIds: google.maps.MapTypeId.SATELLITE
         //mapTypeControlStyle: { style: DEFAULT},
         mapTypeControlOptions: { mapTypeIds: [google.maps.MapTypeId.ROADMAP]},
         mapTypeControl : false
         //disableDefaultUI: true
        // mapTypeControlStyle: {style: DROPDOWN_MENU}

        });

        
     //   heatmap = new google.maps.visualization.HeatmapLayer({
     //    data: getPoints(),
      //    map: google.maps.MapTypeId.SATELLITE
      //  });


         // Add a style-selector control to the map.
         var styleControl = document.getElementById('style-selector-control');
         map.controls[google.maps.ControlPosition.TOP_LEFT].push(styleControl);
 
         // Set the map's style to the initial value of the selector.
         var styleSelector = document.getElementById('style-selector');
         map.setOptions({styles: styles[styleSelector.value]});
 
         // Apply new JSON when the user selects a different style.
         styleSelector.addEventListener('change', function() {
           map.setOptions({styles: styles[styleSelector.value]});
         });

      }

      function toggleHeatmap() {
        heatmap.setMap(heatmap.getMap() ? null : map);
      }

      function changeGradient() {
        var gradient = [
          'rgba(0, 255, 255, 0)',
          'rgba(0, 255, 255, 1)',
          'rgba(0, 191, 255, 1)',
          'rgba(0, 127, 255, 1)',
          'rgba(0, 63, 255, 1)',
          'rgba(0, 0, 255, 1)',
          'rgba(0, 0, 223, 1)',
          'rgba(0, 0, 191, 1)',
          'rgba(0, 0, 159, 1)',
          'rgba(0, 0, 127, 1)',
          'rgba(63, 0, 91, 1)',
          'rgba(127, 0, 63, 1)',
          'rgba(191, 0, 31, 1)',
          'rgba(255, 0, 0, 1)'
        ]
        heatmap.set('gradient', heatmap.get('gradient') ? null : gradient);
      }

      function changeRadius() {
        heatmap.set('radius', heatmap.get('radius') ? null : 20);
      }

      function changeOpacity() {
        heatmap.set('opacity', heatmap.get('opacity') ? null : 0.2);
      }

